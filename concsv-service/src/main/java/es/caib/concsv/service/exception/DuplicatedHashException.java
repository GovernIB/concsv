package es.caib.concsv.service.exception;

public class DuplicatedHashException extends Exception {

	public DuplicatedHashException() {
		super();
	}

	public DuplicatedHashException(String message, Throwable cause) {
		super(message, cause);
	}

	public DuplicatedHashException(String message) {
		super(message);
	}

	public DuplicatedHashException(Throwable cause) {
		super(cause);
	}

	
}
